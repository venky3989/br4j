package com.aplana.ireferent.types;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlType;

@XmlAccessorType(XmlAccessType.FIELD)
@XmlType(name = "WSO_SEARCHREQUEST", propOrder = { 
		"pageNum", 
		"pageSize",
		"keyword" 
})
public class WSOSearchRequest {
	
	@XmlElement(name = "PAGENUM", required = true, nillable = true)
    private String pageNum;
	@XmlElement(name = "PAGESIZE", required = true, nillable = true)
    private String pageSize; 
	@XmlElement(name = "KEYWORD", required = true, nillable = true)
    private String keyword;
	
	public String getPageNum() {
		return pageNum;
	}
	public void setPageNum(String pageNum) {
		this.pageNum = pageNum;
	}
	public String getPageSize() {
		return pageSize;
	}
	public void setPageSize(String pageSize) {
		this.pageSize = pageSize;
	}
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
	
	@Override
    public String toString() {
	return String.format("WSOSearchRequest(pageNum=%s,pageSize=%s, keyword=%s)", pageNum,
			pageSize, keyword);
    }
}